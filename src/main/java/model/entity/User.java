package model.entity;

public class User {
    private String username ;
    private String password ;
    private String email ;

    private String name ;
    private String birthday ;
    private String gender ;
    private int role = 0 ;

    public User(String username, String password, String email, String name ,String birthday, String gender) {
        this.username = username;
        this.password = password;
        this.name = name ;
        this.email = email;
        this.birthday = birthday;
        this.gender = gender;
    }

    public User(String username, String password, String email, String name ,String birthday, String gender, int role) {
        this.username = username;
        this.password = password;
        this.name = name ;
        this.email = email;
        this.birthday = birthday;
        this.gender = gender;
        this.role = role ;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }
}
